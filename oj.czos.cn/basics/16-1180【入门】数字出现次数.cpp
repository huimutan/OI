/*1180 - 【入门】数字出现次数
题目描述
有50个数（0-19），求这50个数中相同数字出现的最多次数为几次？

输入
50个数字

输出
1个数字（即相同数字出现的最多次数）

样例
输入
1 10 2 0 15 8 12 7 0 3 15 0 15 18 16 7 17 16 9 1 19 16 12 17 12 4 3 11 1 14 2 11
14 6 11 4 6 4 11 13 18 7 0 3 2 3 18 19 2 16
输出
4
来源
数组问题

标签
数组问题
*/
#include <bits/stdc++.h>
using namespace std;
int main() {
  int x, max = 0, c[20] = {0};
  for (int i = 0; i < 50; i++) {
    cin >> x;
    c[x]++;
    if (c[x] > max) max = c[x];
  }
  cout << max;
  return 0;
}
