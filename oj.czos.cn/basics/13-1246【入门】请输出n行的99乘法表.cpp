/*【入门】请输出n行的9*9乘法表
题目描述
请从键盘读入一个整数n，代表有n行，输出n行的9*9乘法表。
比如，假设n=5，则输出如下：

1*1=1
2*1=2 2*2=4
3*1=3 3*2=6 3*3=9
4*1=4 4*2=8 4*3=12 4*4=16
5*1=5 5*2=10 5*3=15 5*4=20 5*5=25
输入
一个整数n（n<=9）

输出
n行的9*9乘法表

样例
输入
3
输出
1*1=1
2*1=2 2*2=4
3*1=3 3*2=6 3*3=9
*/
#include <bits/stdc++.h>
using namespace std;
int main() {
  int n;
  cin >> n;
  for (int i = 1; i <= n; i++) {  //循环n行
    for (int j = 1; j <= i; j++) {
      cout << i << "*" << j << "=" << i * j << " ";
    }
    cout << endl;
  }
  return 0;
}
