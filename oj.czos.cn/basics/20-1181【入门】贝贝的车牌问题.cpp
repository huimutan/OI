/*1181 - 【入门】贝贝的车牌问题
题目描述
广州市车管所为每一辆入户的汽车都发放一块车牌，车牌的号码由六个字符组成，
如A99452、B88888等，这个字符串从左边数起的第一个字符为大写英文字母，
如A、B、C等，表示这辆车是属于广州市区内的汽车还是郊区的汽车，后面的五位由数字组成。
假定以字母A、B、C、D、E、F、G、R、S、T开头的表示是市区车牌，
而以其他字母开头的表示郊区车牌。
车管所把这个任务交给贝贝。请你帮贝贝找出所给出的车牌中有多少辆是广州郊区的汽车。

输入
第1行是一个正整数N（1<=N<=10^6），表示共有N个车牌。
接下来的N行，每行是一个车牌号。题目保证给出的车牌不会重复。

输出
只有1行，即广州郊区车牌的数量。

样例
输入
3
G54672
Q87680
P77771
输出
2
来源
字符串

标签
字符串
*/
#include <bits/stdc++.h>
using namespace std;
int main() {
  int n, c = 0;
  string s;
  cin >> n;
  for (int i = 1; i <= n; i++) {
    cin >> s;
    if (s[0] > 'G' && s[0] < 'R' || s[0] > 'T') c++;
  }
  cout << c;
  return 0;
}
