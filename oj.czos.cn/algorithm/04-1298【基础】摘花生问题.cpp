/*1298 - 【基础】摘花生问题
题目描述
Hello Kitty
想摘点花生送给她喜欢的米老鼠。她来到一片有网格状道路的矩形花生地(如下图)，从西北角进去，东南角出来。地里每个道路的交叉点上都有种着一株花生苗，上面有若干颗花生，经过一株花生苗就能摘走该它上面所有的花生。Hello
Kitty只能向东或向南走，不能向西或向北走。问Hello Kitty 最多能够摘到多少颗花生。


如输入：
2 2
1 1
3 4
代表有2行，每行有2株花生，那么摘能摘到的最多的花生就是：1->3->4，总和为8颗花生。

输入
第一行是两个整数m和n（m<=100，n<=100），代表了花生地里有m行，每行有n列的花生！

后面m行，每行有n个整数代表了每行中，每株花生的数量！



输出
输出是一个整数，代表了最多能摘到的花生的总数

样例
输入
2 2
1 1
3 4
输出
8
来源
递推

标签
递推
*/
#include <bits/stdc++.h>
using namespace std;
int a[110][110];
int main() {
  int m, n, i, j;
  cin >> m >> n;
  for (i = 1; i <= m; i++) {
    for (j = 1; j <= n; j++) {
      cin >> a[i][j];
    }
  }
  //数组通过计算存储当前累计的最大值：当前最大值= 当前值 +
  //(左边值和上面值中的较大值)
  for (i = 1; i <= m; i++) {
    for (j = 1; j <= n; j++) {
      a[i][j] += max(a[i][j - 1], a[i - 1][j]);
    }
  }
  cout << a[m][n] << endl;
  return 0;
}
