/*1271 - 【基础】高精度整数除法
题目描述
求a/b的结果。 已知a，b为10^8范围内的非负整数，求a/b保留前n位小数商的结果。
（5.1.72）

输入
a b n

输出
一行数字

样例
输入
97 61 50
输出
1.59016393442622950819672131147540983606557377049180
来源
高精度算法

标签
高精度算法
*/
#include <bits/stdc++.h>
using namespace std;
int main() {
  int a, b, i, n, t;
  cin >> a >> b >> n;
  cout << a / b << ".";
  t = a % b;
  for (i = 1; i <= n; i++) {
    t *= 10;
    cout << t / b;
    t %= b;
  }
}
