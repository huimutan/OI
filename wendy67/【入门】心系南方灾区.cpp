/*E. 【入门】心系南方灾区
题目描述
2008年年初我国南方正在承受百年不遇的大雪、冻雨灾害。
北京市已经开始了面向全体市民的捐款捐物活动，并组织运力，
以最快速度将这些救灾物资运送到灾区人民的手中。
 已知救灾物资中有m件大衣（10000 <= m <= 2000000 ），
 一辆卡车一次最多可以运走n件（2000 <= n <= 10000）。
 请你编写程序计算一下，要将所有的大衣运走，北京市政府最少需要调动多少辆卡车参与运送。

输入
只有两个整数 m、n 。

输出
只有一个整数，表示需要的卡车数量。
*/
#include <bits/stdc++.h>
using namespace std;
int main() {
  int m, n;
  cin >> m >> n;
  if (m > n) {
    if (m % n == 0) {
      cout << m / n;
    } else {
      cout << m / n + 1;
    }
  } else {
    cout << 1;
  }

  return 0;
}
